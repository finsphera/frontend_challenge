// Define a type for Movie objects
export type Movie = {
  id: number; // Unique identifier for the movie
  title: string; // Title of the movie
  overview: string; // Overview or summary of the movie
  poster_path: string; // Path to the poster image of the movie
  backdrop_path: string; // Path to the backdrop image of the movie
  release_date: string; // Release date of the movie
  vote_average: number; // Average vote rating of the movie
};
